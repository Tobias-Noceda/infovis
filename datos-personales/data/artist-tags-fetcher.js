const API_KEY = process.env.LASTFM_API_KEY;

if (!API_KEY) {
    console.error('Error: LASTFM_API_KEY not found in .env file');
    process.exit(1);
}

const errors = [];
const results = [];

// Function to make HTTPS request
function fetchArtistTags(artistName) {
    return new Promise((resolve, reject) => {
        const url = `https://ws.audioscrobbler.com/2.0/?method=artist.gettoptags&artist=${encodeURIComponent(artistName)}&api_key=${API_KEY}&format=json`;

        https.get(url, (res) => {
            let data = '';

            res.on('data', (chunk) => {
                data += chunk;
            });

            res.on('end', () => {
                try {
                    const jsonData = JSON.parse(data);

                    // Check if there's an error in the response
                    if (jsonData.error) {
                        reject(new Error(jsonData.message || 'Unknown error from API'));
                    }

                    // Extract top 3 tags
                    const tags = jsonData.toptags?.tag || [];
                    const topTags = tags
                        .slice(0, 3)
                        .map(tag => tag.name)
                        .join(',');

                    resolve({
                        artist: artistName,
                        tags: topTags
                    });
                } catch (e) {
                    reject(new Error(`Failed to parse JSON response: ${e.message}`));
                }
            });
        }).on('error', (error) => {
            reject(new Error(`Network error: ${error.message}`));
        });
    });
}

// Main execution
async function main() {
    console.log('artistName,tag1,tag2,tag3');

    for (const artist of artists) {
        try {
            const result = await fetchArtistTags(artist);
            console.log(`${result.artist},${result.tags}`);
            results.push(result);
        } catch (error) {
            console.error(`[Error] ${artist}: ${error.message}`);
            errors.push({
                artist: artist,
                error: error.message
            });
        }
        setTimeout(() => {}, 350); // Small delay to avoid hitting API rate limits
    }

    // Write results to file
    if (results.length > 0) {
        const resultCsv = ['artistName,tag1,tag2,tag3']
            .concat(results.map(r => `${r.artist},${r.tags.replace(/"/g, '""')}`))
            .join('\n');

        fs.writeFileSync('artist_tags.csv', resultCsv);
        console.log(`\n${results.length} result(s) written to artist_tags.csv`);
    }
    // Write errors to file if any occurred
    if (errors.length > 0) {
        const errorCsv = ['artistName,error']
            .concat(errors.map(e => `${e.artist},${e.error.replace(/"/g, '""')}`))
            .join('\n');

        fs.writeFileSync('errors.csv', errorCsv);
        console.error(`\n${errors.length} error(s) written to errors.csv`);
    }
}

main();

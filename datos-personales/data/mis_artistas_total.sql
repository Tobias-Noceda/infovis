-- Update genre_group with grouped genres based on the original genre
UPDATE mis_artistas_total SET genre_group = 
  CASE 
    WHEN genre IS NULL THEN NULL

    -- POP
    WHEN genre ILIKE '%pop%' THEN 'Pop'
    
    -- ROCK
    WHEN genre ILIKE '%rock%' OR genre ILIKE '%grunge%' OR genre ILIKE '%rocanrol%' THEN 'Rock'

    -- PUNK
    WHEN genre ILIKE '%punk%' THEN 'Punk'

    -- indie and related genres
    WHEN genre ILIKE '%indie%' OR genre ILIKE '%new wave%' THEN 'Indie'

    -- CHILL
    WHEN genre ILIKE '%chill%' OR genre ILIKE '%lo-fi%' OR genre ILIKE '%relax%' OR genre ILIKE '%ambient%' THEN 'Chill'
    
    -- ELECTRONIC/TECHNO
    WHEN genre ILIKE '%electro%' OR genre ILIKE '%trance%' OR genre ILIKE '%techno%' OR genre ILIKE '%dubstep%' OR genre ILIKE '%EDM%' OR genre ILIKE '%hardstyle%' OR genre ILIKE '%electro%' OR genre ILIKE '%drumstep%' OR genre ILIKE '%downtempo%' OR genre ILIKE '%Grime%' OR genre = 'dub' THEN 'Electronic'

    -- METAL
    WHEN genre ILIKE '%metal%' OR genre ILIKE '%hardcore%' OR genre ILIKE '%sludge%' THEN 'Metal'
    
    -- DANCE
    WHEN genre IN ('dance', 'eurodance') OR genre ILIKE '%dance%' OR genre ILIKE '%rkt%' OR genre ILIKE '%disco%' OR genre ILIKE '%baile%' OR genre ILIKE '%fiesta%' OR genre ILIKE '%joda%' THEN 'Dance'

    -- HIP-HOP / RAP
    WHEN genre ILIKE  '%hop%' OR genre ILIKE '%rap%' OR genre ILIKE '%urban%' OR genre ILIKE '%beat%' THEN 'Hip-Hop'
    
    -- SKA / REGGAE
    WHEN genre IN ('ska', 'reggae', 'rocksteady', 'tropipop', 'troika') THEN 'Ska/Reggae'

    -- LATIN (Regional latin styles)
    WHEN genre ILIKE '%corridos%' OR genre IN ('latin', 'latino', 'Reggaeton', 'cumbia', 'dembow', 'salsa', 'bachata', 'merengue', 'corridos', 'tango', 'tango argentino', 'candombe', 'mariachi', 'folclore', 'folklore argentino', 'samba', 'Bossa Nova', 'estilo cumbia', 'reggaeton', 'reggae', 'Reggaeton', 'raggaeton', 'bachata', 'merengue', 'ranchera', 'corridos', 'bolero', 'balada', 'trovador', 'Cuarteto', 'cumbia cheta', '420') THEN 'Latin'
    
    -- Reggae
    WHEN genre ILIKE '%reggae%' THEN 'Reggae'

    -- Brazilian
    WHEN genre ILIKE '%brazil%' OR genre ILIKE '%brasil%' OR genre ILIKE '%bossa nova%' OR genre ILIKE '%mpb%' OR genre ILIKE '%samba%' OR genre ILIKE '%pagode%' OR genre ILIKE '%axe%' OR genre ILIKE '%sertanejo%' OR genre ILIKE '%trova%' OR genre ILIKE '%troika%' THEN 'Brazilian'

    -- JAZZ
    WHEN genre IN ('jazz', 'jazz-rock', 'Smooth Jazz', 'free jazz', 'Jazz Rock') THEN 'Jazz'
    
    -- SOUL / R&B
    WHEN genre ILIKE '%blues%' OR genre IN ('soul', 'rnb', 'r&b', 'Neo-Soul', 'soul', 'neo-soul') THEN 'Soul/R&B/Blues'
    
    -- Funk
    WHEN genre ILIKE '%funk%' OR genre ILIKE '%phonk%' THEN 'Funk'

    -- CLASSICAL
    WHEN genre IN ('Classical', 'contemporary classical', 'orchestral', 'classical guitar', 'Classical', 'contemporary classical', 'orchestral', 'orchestra') THEN 'Classical'
    
    -- CHRISTIAN / WORSHIP
    WHEN genre ILIKE '%christian%' OR genre ILIKE '%worship%' OR genre ILIKE '%adoracion%' OR genre ILIKE '%alabanza%' OR genre ILIKE '%cristi%' OR genre ILIKE '%gospel%' OR genre ILIKE '%jesucristo%' OR genre ILIKE '%catholic%' THEN 'Christian'
    
    -- ACUSTIC / INSTRUMENTAL
    WHEN genre ILIKE '%acoustic%' OR genre ILIKE '%melodic%' OR genre ILIKE '%soft%' OR genre ILIKE '%balad%' OR genre ILIKE '%ballad%' OR genre ILIKE '%guitar%' OR genre ILIKE '%piano%' OR genre ILIKE '%bass%' OR genre ILIKE '%instrumental%' OR genre ILIKE '%wood%' OR genre ILIKE '%string%' OR genre ILIKE '%spoken%' OR genre ILIKE '%cover%' OR genre ILIKE '%vocal%' OR genre ILIKE '%a cappella' THEN 'Acoustic/Instrumental'

    -- FOLK
    WHEN genre ILIKE '%folk%' THEN 'Folk'
    
    -- AMBIENT / CHILL / RELAXING
    WHEN genre IN ('ambient', 'chillout', 'lo-fi', 'lofi', 'relaxing', 'relax', 'sleep aid', 'Sleep', 'calm', 'chillhop', 'meditacion', 'Lo-Fi', 'ambient', 'downtempo', 'relaxing', 'sleep aid', 'relax', 'calm', 'nature', 'new age', 'downtempo', 'calm', 'meditacion', 'sleep', 'low fi', 'paz', 'Nature', 'lounge', 'Study', 'cute') THEN 'Chill'
    
    -- CHILDREN / FAMILY
    WHEN genre IN ('Disney', 'kids', 'infantil', 'cartoon', 'children', 'infantil- juvenil- brasileira', 'disney channel', 'broadway', 'Hairspray', 'glee', 'west end', 'Walt Disney Studio Orchestra', 'children', 'kids', 'infantil', 'j-pop', 'kids', 'infancia', 'for children', 'comedy', 'maestros', 'showtunes', 'comic', 'childrens', 'gaming') THEN 'Children'
    
    -- EXPERIMENTAL / ALTERNATIVE
    WHEN genre ILIKE '%experimental%' OR genre ILIKE '%alternative%' OR genre ILIKE '%psychedelic%' OR genre ILIKE '%progressive%' THEN 'Alternative'

    -- musical
    WHEN genre ILIKE '%musical%' OR genre ILIKE '%theatre%' OR genre ILIKE '%play%' OR genre IN ('Harispray', 'Theatre', 'Broadway') THEN 'Musical'

    WHEN genre ILIKE '%country%' THEN 'Country'

    ELSE genre
  END;

SELECT genre, genre_group, COUNT(*) AS count
FROM mis_artistas_total
WHERE genre IS NOT NULL
GROUP BY genre, genre_group
ORDER BY count DESC;


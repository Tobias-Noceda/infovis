const artists = [];

// Helper function to add delay between requests
const delay = (ms) => new Promise(resolve => setTimeout(resolve, ms));

// Process artists sequentially with 2 second delay between requests
for (const artistName of artistNames) {
    const query = encodeURIComponent(artistName);
    const url = `https://api.spotify.com/v1/search?q=${query}&type=artist`;
    
    try {
        const response = await fetch(url, {
            headers: {
                'Authorization': `Bearer ${token}`,
            },
        });
        
        const data = await response.json();
        
        if (data.error) {
            console.error(`Error para ${artistName}:`, data.error.message);
        } else if (data.artists.items.length > 0) {
            const artist = data.artists.items[0];
            const artistURL = `https://api.spotify.com/v1/artists/${artist.id}`;

            try {
                const artistResponse = await fetch(artistURL, {
                    headers: {
                        'Authorization': `Bearer ${token}`,
                    },
                });
                const artistData = await artistResponse.json();
                if (artistData.error) {
                    console.error(`Error al obtener detalles de ${artistName}:`, artistData.error.message);
                } else {
                    // console.log(`Detalles de ${artistName}:`, artistData);
                    artists.push({
                        name: artist.name,
                        genres: artistData.genres,
                        spotifyId: artist.id,
                        url: artist.external_urls.spotify,
                    });
                }

                console.log(`✓ ${artistName} encontrado`);
            } catch (artistError) {
                console.error(`Error al obtener detalles de ${artistName}:`, artistError.message);
            }
        } else {
            console.warn(`✗ No se encontró el artista: ${artistName}`);
        }
    } catch (error) {
        console.error(`Error al buscar el artista ${artistName}:`, error.message);
    }
    
    // Wait 2 seconds before next request (adjust as needed: 1000ms = 1s, 3000ms = 3s)
    await delay(Math.random() * 1000 + 1000); // Random delay between 1-3 seconds
}

console.log('Artistas encontrados:', JSON.stringify(artists));
